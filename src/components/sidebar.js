export async function renderSidebar() {
  const currentPage = window.location.pathname.split('/').pop() || 'index.html';
  const allowedPages = await getAllowedPages();
  const userIsAdmin = await isSuperAdmin();

  // Build sections, filtering children by allowed pages
  const visibleSections = navSections
    .map(sec => {
      let children = sec.children.filter(child => {
        if (!child.slug) return true;
        if (allowedPages === null) return true; // admin sees all
        return allowedPages.includes(child.slug);
      });
      // Inject Admin Panel into Settings & Admin section for super admins
      if (sec.section === 'Settings & Admin' && userIsAdmin) {
        children = [...children, adminNavItem];
      }
      return { ...sec, children };
    })
    .filter(sec => sec.children.length > 0);

  // Build nav HTML: one group per section
  const navHTML = visibleSections.map(sec => {
    // Determine if any child in this section is the current page
    const sectionActive = sec.children.some(
      c => c.href && c.href.replace('./', '') === currentPage
    );

    const childLinksHTML = sec.children.map(child => {
      const isActive = child.href && child.href.replace('./', '') === currentPage ? 'active' : '';
      return `<a href="${child.href}" class="sidebar-flyout-item ${isActive}">
        <span class="sidebar-flyout-item-icon">${child.icon}</span>
        <span>${child.label}</span>
      </a>`;
    }).join('');

    return `
      <div class="sidebar-section-group${sectionActive ? ' has-active' : ''}">
        <div class="sidebar-section-trigger">
          <span class="sidebar-section-trigger-icon">${sec.sectionIcon}</span>
          <span class="sidebar-section-trigger-label">${sec.section}</span>
          <span class="sidebar-section-trigger-chevron">
            <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="2" stroke="currentColor" style="width:14px;height:14px;"><path stroke-linecap="round" stroke-linejoin="round" d="m8.25 4.5 7.5 7.5-7.5 7.5" /></svg>
          </span>
        </div>
        <div class="sidebar-flyout">
          <div class="sidebar-flyout-header">${sec.section}</div>
          ${childLinksHTML}
        </div>
      </div>`;
  }).join('');

  const sidebar = document.createElement('div');
  sidebar.innerHTML = `
    <div class="sidebar-overlay" id="sidebar-overlay"></div>
    <aside class="sidebar" id="sidebar">
      <div class="sidebar-header">
        <img src="${sidebarIcon}" alt="TalentLedger" style="width:40px;height:40px;border-radius:8px;object-fit:cover;object-position:center 25%;">
        <span class="sidebar-app-name">TalentLedger</span>
      </div>
      <nav class="sidebar-nav">${navHTML}</nav>
    </aside>
  `;

  document.body.prepend(sidebar);

  // Mobile overlay close
  document.getElementById('sidebar-overlay').addEventListener('click', () => {
    document.getElementById('sidebar').classList.remove('open');
    document.getElementById('sidebar-overlay').classList.remove('open');
  });

  // Position each flyout vertically to align with its trigger row.
  // Uses fixed positioning (to escape sidebar overflow:auto clipping).
  // Run once on load and recalculate on scroll/resize.
  function positionFlyouts() {
    document.querySelectorAll('.sidebar-section-group').forEach(group => {
      const trigger = group.querySelector('.sidebar-section-trigger');
      const flyout  = group.querySelector('.sidebar-flyout');
      if (!trigger || !flyout) return;
      const rect = trigger.getBoundingClientRect();
      flyout.style.top = rect.top + 'px';
    });
  }

  positionFlyouts();

  // Re-position on sidebar scroll and window resize
  document.getElementById('sidebar')?.addEventListener('scroll', positionFlyouts);
  window.addEventListener('resize', positionFlyouts);

  // On mobile: tap to toggle flyout open/closed
  document.querySelectorAll('.sidebar-section-trigger').forEach(trigger => {
    trigger.addEventListener('click', () => {
      const group = trigger.closest('.sidebar-section-group');
      group.classList.toggle('mobile-open');
    });
  });
}

export function toggleSidebar() {
  document.getElementById('sidebar').classList.toggle('open');
  document.getElementById('sidebar-overlay').classList.toggle('open');
}
